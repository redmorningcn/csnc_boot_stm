/*******************************************************************************
 * 名    称： AppGetCrc16Chk
 * 功    能： 取CRC16校验和（长沙南车协议CRC校验）
 * 入口参数： 无
 * 出口参数： 无
 * 作    者： 无名沈.
 * 创建日期： 2017-07-26
 * 修    改：
 * 修改日期：
 *******************************************************************************/
u16 GetCrc16Chk(u8 *buf, u16 len)
{
    u16 crc = 0x0;
    u16 i, tmp;
    
    for (i=0; i< len; i++) {
        tmp = crc;
        crc = APP_SD_CRC16_TAB[(tmp>>8) ^ *buf++];
        crc = crc ^ (tmp<<8);
    }
    
    return(crc);
}
