u64 GetCrc32Chk(u64 m_CRC, u8  *buf, u16 len)
{
    u8 index;
    u16 i;
    
    for ( i = 0; i < len; i++ ) {
        index           = (u8)((m_CRC ^ buf[i]) & 0xff);
        m_CRC           = CRC32_TAB[index] ^ (m_CRC >> 8);
    }
    //return m_CRC ^ 0x00000000ffffffffL;
    return m_CRC;
}