/*******************************************************************************
 * 名    称： AppGetMBCrc16Chk
 * 功    能： 
 * 入口参数： 无
 * 出口参数： 无
 * 作    者： 无名沈.
 * 创建日期： 2017-07-26
 * 修    改：
 * 修改日期：
 *******************************************************************************/
u16 GetMBCrc16Chk(u8 *buf, u16  len)
{
    u8 uchCRCHi = 0xFF ;                           /* 高CRC字节初始化 */
    u8 uchCRCLo = 0xFF ;                           /* 低CRC 字节初始化 */
    unsigned uIndex ;                                   /* CRC循环中的索引 */
    
    while (len--) {                                     /* 传输消息缓冲区 */
        uIndex = uchCRCHi ^ *buf++ ;                    /* 计算CRC */
        uchCRCHi = uchCRCLo ^ APP_AUCH_CRC_HI[uIndex];
        uchCRCLo = APP_AUCH_CRC_LO[uIndex] ;
    }
    
    return (uchCRCHi << 8 | uchCRCLo) ;
} 
